#include "core/platforms/debugging/windows/debugger_windows.h"

#include "core/shared/logging/log_message.h"

#include <windows.h>

namespace core
{
	bool DEBUGGER_WINDOWS::is_present() const
	{
		return IsDebuggerPresent() != FALSE;
	}

	void DEBUGGER_WINDOWS::pause()
	{
		if (is_present())
		{
			__debugbreak();
		}
	}
}
